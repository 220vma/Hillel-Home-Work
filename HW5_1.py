l = [
	[1, 2, 3, 4, 5, 6],
	[10, 21, 13, 14, 52, 63, 34],
	[2],
	[3, 2, 6],
	[35, 47],
	[-3, -2, -1, 0, 1, 2, 3, 4, 5, 6],
	[100, 200, 300, 400, 500, 600]
]
total = 0

for i in range(len(l)):
    for j in range(len(l[i])):
        if l[i][j] != 0 and l[i][j] % 2 != 0:
            total += l[i][j]

print(f'Сумма всех нечетных чисел каждой строки: {total}')